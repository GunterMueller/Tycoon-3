/*
 *  View tail of very large tycoon2.dbg
 */

#include <io.h>
#include <stdio.h>

FILE * debugFile;

void main() {
	char ch;
	debugFile = fopen("tycoon2.dbg", "rt");
	fseek(debugFile, -10000, SEEK_END);
	ch = fgetc(debugFile);
	while (ch != EOF) {
		fputc(ch, stdout);
	    ch = fgetc(debugFile);
	}
}
